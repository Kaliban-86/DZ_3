# Методы списков - append / remove / insert / sort / count
test_lis = []
test_lis.append('first method')
test_lis.append('second method')
print(test_lis)

test_lis.remove('first method')
print(test_lis)

test_lis.insert(1, 'first method')
print(test_lis)

test_lis.sort()
print(test_lis)

print(test_lis.count('second method'))

# Методы строк upper / lower / split / find / isdigit

test_string = 'Hellow new world'
print(test_string)
print(test_string.upper())
print(test_string.lower())

print(test_string.split(' '))
print(test_string.find('new'))
print(test_string.isdigit())

